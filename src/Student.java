
public class Student {
 int stId;
 String name,lastName;
public Student( String name, String lastName) {
	this.stId = stId;
	this.name = name;
	this.lastName = lastName;
}

@Override
public String toString() {
	return "Student [stId=" + stId + ", name=" + name + ", lastName=" + lastName + "]";
}

 
}
